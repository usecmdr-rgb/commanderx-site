// Check if workflow condition matches
function checkCondition(condition: Workflow["condition"], context: any): boolean {
  if (!condition) return true;

  const { field, operator, value } = condition;
  const fieldValue = context[field];

  switch (operator) {
    case "contains":
      return fieldValue?.toString().toLowerCase().includes(value?.toString().toLowerCase() || "");
    case "equals":
      return fieldValue === value;
    case "greaterThan":
      return Number(fieldValue) > Number(value);
    case "lessThan":
      return Number(fieldValue) < Number(value);
    case "exists":
      return fieldValue !== undefined && fieldValue !== null;
    default:
      return false;
  }
}

export async function POST(request: NextRequest) {
  try {
    const body = await request.json();
    const { workflowId, trigger, context = {} } = body;

    if (!workflowId && !trigger) {
      return NextResponse.json(
        { error: "Either workflowId or trigger is required" },
        { status: 400 }
      );
    }

    const supabase = getSupabaseServerClient();
    let workflow: Workflow | null = null;

    // If workflowId provided, fetch the workflow
    if (workflowId) {
      const { data, error } = await supabase
        .from("workflows")
        .select("*")
        .eq("id", workflowId)
        .eq("enabled", true)
        .single();

      if (error && error.code !== "PGRST116") {
        throw error;
      }

      workflow = data;
    }

    // If trigger provided, find matching workflows
    if (trigger && !workflow) {
      const { data, error } = await supabase
        .from("workflows")
        .select("*")
        .eq("trigger", trigger)
        .eq("enabled", true);

      if (error && error.code !== "PGRST116") {
        throw error;
      }

      // Find workflow that matches condition
      if (data && data.length > 0) {
        workflow = data.find((w) => checkCondition(w.condition, context)) || data[0];
      }
    }

    if (!workflow) {
      return NextResponse.json(
        { error: "No matching workflow found" },
        { status: 404 }
      );
    }

    // Check condition if workflow has one
    if (workflow.condition && !checkCondition(workflow.condition, context)) {
      return NextResponse.json({
        ok: true,
        message: "Workflow condition not met",
        executed: false,
      });
    }

    // Execute all actions
    const results = [];
    for (const action of workflow.actions) {
      let result;
      
      if (action.startsWith("aloha.")) {
        result = await executeAlohaAction(action, context);
      } else if (action.startsWith("sync.")) {
        result = await executeSyncAction(action, context);
      } else if (action.startsWith("studio.")) {
        result = await executeStudioAction(action, context);
      } else if (action.startsWith("insight.")) {
        result = await executeInsightAction(action, context);
      } else {
        result = { success: false, message: `Unknown action: ${action}` };
      }

      results.push({
        action,
        ...result,
      });
    }

    return NextResponse.json({
      ok: true,
      workflowId: workflow.id,
      workflowName: workflow.name,
      executed: true,
      results,
    });
  } catch (error: any) {
    console.error("Error running workflow:", error);
    return NextResponse.json(
      { ok: false, error: error.message || "Failed to run workflow" },
      { status: 500 }
    );
  }
}





